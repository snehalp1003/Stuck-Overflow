    private void changePasswordJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordJButtonActionPerformed
        String username = usernameJTextField.getText();
        String oldPassword = String.valueOf(oldPasswordJPasswordField.getPassword());
        String newPassword = String.valueOf(newPasswordJPasswordField.getPassword());
        String confirmPassword = String.valueOf(confirmPasswordJPasswordField.getPassword());
        
        if (oldPassword.equals(newPassword)) {
            JOptionPane.showMessageDialog(null, "New and old passwords cannot be same !");
        } else if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(null, "Passwords don't match !");
        } else {
            organization.getUserAccountDirectory().changeUserPassword(username, oldPassword, newPassword);
            JOptionPane.showMessageDialog(null, "Password updated !");
            oldPasswordJPasswordField.setText("");
            newPasswordJPasswordField.setText("");
            confirmPasswordJPasswordField.setText("");
        }
    }//GEN-LAST:event_changePasswordJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed


