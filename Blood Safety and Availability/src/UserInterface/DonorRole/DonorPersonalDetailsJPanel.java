    private void populateUserDetails() {
        nameJTextField.setText(oldDonor.getDonorName());
        usernameJTextField.setText(userAcc.getUsername());

        if (oldDonor.getContact() != null) {
            contactNoJTextField.setText(oldDonor.getContact().toString());
        } else {
            contactNoJTextField.setText("");
        }
        

        if (oldDonor.getGender() != null) {
            genderJComboBox.setSelectedItem(oldDonor.getGender());
        } else {
            genderJComboBox.setSelectedItem("");
        }

        if (oldDonor.getDOB() != null) {
            DOBDatePicker.setDate(oldDonor.getDOB());
        } else {
            DOBDatePicker.setDate(null);
        }

        if (oldDonor.getDonorBloodType() != null) {
            bloodGroupJComboBox.setSelectedItem(oldDonor.getDonorBloodType());
        } else {
            bloodGroupJComboBox.setSelectedItem("");
        }
        
        if (oldDonor.getDonorHeight() != null)
        {
            HtjTextField.setText(oldDonor.getDonorWeight().toString());
        }
        else
        {
            HtjTextField.setText("");
        }
        
        if (oldDonor.getDonorWeight() != null)
        {
            WeightjTextField.setText(oldDonor.getDonorWeight().toString());
        }
        else
        {
            WeightjTextField.setText("");
        }
    }
    
    private boolean ContactPatternCorrect() {

        Pattern p = Pattern.compile("^[0][1-9]\\d{9}$|^[1-9]\\d{9}$");
        Matcher m = p.matcher(contactNoJTextField.getText());
        boolean b = m.matches();
        return b;
    }
    
    private boolean HeightPatternCorrect() {

        String ht = HtjTextField.getText();
//        Pattern p = Pattern.compile("^[0-9]+\\.[0-9]+$");
        Pattern p = Pattern.compile("^[0-9]+$");
        Matcher m = p.matcher(ht);
        boolean b = m.matches();
        return b;
    }
    
    private boolean WeightPatternCorrect() {

        String ht = HtjTextField.getText();
//        Pattern p = Pattern.compile("^[0-9]+\\.[0-9]+$");
        Pattern p = Pattern.compile("^[0-9]+$");
        Matcher m = p.matcher(ht);
        boolean b = m.matches();
        return b;
    }


