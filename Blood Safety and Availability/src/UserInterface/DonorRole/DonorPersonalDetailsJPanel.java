    private void SaveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveJButtonActionPerformed
     
        String donorNo = contactNoJTextField.getText();
        
        if (donorNo == null || !ContactPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Please enter a valid 10 digit contact number !");
            return;
        }
        
        if (HeightPatternCorrect())
        {
            try
            {
                Double.parseDouble(HtjTextField.getText());
            }
            catch(NumberFormatException nfe)
            {
                JOptionPane.showMessageDialog(null, "Please enter valid donor's height in numbers !");
            }
            catch (Exception e)
            {
                  JOptionPane.showMessageDialog(null, "Please enter valid donor's height in numbers !");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please enter valid donor's height !");
        }
         /*Double height = Double.parseDouble(HtjTextField.getText());
        if (height == null || HtjTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter valid donor's height !");
            return;
        }*/
         
         if (WeightPatternCorrect())
        {
            try
            {
                Double.parseDouble(WeightjTextField.getText());
            }
            catch(NumberFormatException nfe)
            {
                JOptionPane.showMessageDialog(null, "Please enter valid donor's weight in numbers !");
            }
            catch (Exception e)
            {
                  JOptionPane.showMessageDialog(null, "Please enter valid donor's weight in numbers !");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please enter valid donor's weight !");
        }
        
       /* Double weight = Double.parseDouble(WeightjTextField.getText());
        if (weight == null || WeightjTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter valid patient's weight !");
            return;
        }*/
        
        String donorGender = genderJComboBox.getSelectedItem().toString();
        if (donorGender == null || donorGender.equals("")) {
            JOptionPane.showMessageDialog(null, "Please select donor's gender !");
            return;
        }
        
        String donorBloodGroup = bloodGroupJComboBox.getSelectedItem().toString();
        if (donorBloodGroup == null || donorBloodGroup.equals("")) {
            JOptionPane.showMessageDialog(null, "Please select donor's blood group !");
            return;
        }
        Date today = new Date();
        Date donorDOB = DOBDatePicker.getDate();
        if (donorDOB == null || donorDOB.after(today)) {
            JOptionPane.showMessageDialog(null, "Please select valid date of birth for donor !");
            return;
        }
        
        Double donorwt = Double.parseDouble(WeightjTextField.getText());
        Double donorht = Double.parseDouble(HtjTextField.getText());

        for (Donor donor: organization.getDonorDirectory().getDonorList())
        {
            if(donor.getDonorName().equals(oldDonor.getDonorName())&&
                    //donor.getDonorRegisteredDate()==null||  (donor.getDonorRegisteredDate().equals(oldDonor.getDonorRegisteredDate()))&&
                    oldDonor.getDOB() == null || (donor.getDOB().equals(oldDonor.getDOB()))&&
                    oldDonor.getGender()== null || (donor.getGender().equals(oldDonor.getGender()))&&
                    donor.getDonorBloodType()== null || (donor.getDonorBloodType().equals(oldDonor.getDonorBloodType()))&&
                    donor.getContact() == null || (donor.getContact().equals(oldDonor.getContact()))&&
                    donor.getDonorWeight() == null || (donor.getDonorWeight().equals(oldDonor.getDonorWeight()))&&
                    donor.getDonorHeight() == null || (donor.getDonorHeight().equals(oldDonor.getDonorHeight())))
            {
                updatedDonor = donor;
            }
        }
        
        organization.getDonorDirectory().getDonorList().remove(updatedDonor);
        organization.getUserAccountDirectory().getUserAccountList().remove(userAcc);
        
        updatedDonor.setContact(donorNo);
        updatedDonor.setGender(donorGender);
        updatedDonor.setDOB(donorDOB);
        updatedDonor.setDonorBloodType(donorBloodGroup);
        updatedDonor.setDonorWeight(donorwt);
        updatedDonor.setDonorHeight(donorht);
        
        updatedUserAccount.setDonor(updatedDonor);
        
        organization.getDonorDirectory().getDonorList().add(updatedDonor);
        organization.getUserAccountDirectory().getUserAccountList().add(updatedUserAccount);
         JOptionPane.showMessageDialog(null, "Saved Donor information !");
         

       
        
    }//GEN-LAST:event_SaveJButtonActionPerformed


